import 'dart:ffi';

class Details{

  final String name;
  final String designation;
  final String address;
  final String phone;
  final bool isread;

  final String email;
  final String sms;
  final String imagePath;

  Details({
    required this.name,
    required this.designation,
    required this.address,
    required this.phone,
    required this.isread,
    required this.email,
    required this.sms,
    required this.imagePath,
  });



}